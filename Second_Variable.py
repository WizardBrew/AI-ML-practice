#Variables 

a = 10;  # A Saves Value of 10 -- you can use ; to terminate a statement in python but it is not necessary.
b = 20  # B Saves Value of 20 

c = a + b;  # C Saves Value of 30 as result of a + b. --good practice to use ; to terminate a statement

print(c);  # Prints Value of C which is 30

